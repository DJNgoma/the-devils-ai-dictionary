import fs from "node:fs/promises";
import path from "node:path";
import matter from "gray-matter";
import {
  assertValidEntry,
  buildResolvedEntryReferences,
  buildSearchText,
  compareMisunderstoodEntries,
  createEntryReferenceResolver,
  scoreRelatedEntries,
} from "../src/lib/content-build.mjs";
import {
  categoryDefinitions,
} from "../src/lib/content-catalog.mjs";
import {
  createCatalogVersion,
  createCatalogSnapshot,
  publishMobileCatalogArtifacts,
  serializeCatalogSnapshot,
} from "../src/lib/mobile-catalog.mjs";

const root = process.cwd();
const entriesDirectory = path.join(root, "content", "entries");
const outputDirectory = path.join(root, "src", "generated");
const outputFile = path.join(outputDirectory, "entries.generated.json");
const webOutputFile = path.join(outputDirectory, "entries.web.generated.json");
const detailsShardDirectory = path.join(outputDirectory, "entry-details");
const detailsShardLoaderFile = path.join(outputDirectory, "entry-detail-shards.generated.ts");
const publicCatalogDirectory = path.join(root, "public", "catalog");
const publicMobileCatalogDirectory = path.join(root, "public", "mobile-catalog");
const editorialTimeZone = "Africa/Johannesburg";
const schemaVersion = 1;

/* ---------- helpers ---------- */

function slugify(value) {
  return value
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function entryDetailShardKey(entry) {
  const key = String(entry.letter ?? "").trim().toLowerCase();
  return /^[a-z]$/.test(key) ? key : "other";
}

function buildEntryDetailShardLoaderSource(shardKeys) {
  const entries = shardKeys
    .map(
      (key) =>
        `  ${JSON.stringify(key)}: () => import(${JSON.stringify(`./entry-details/${key}.json`)}),`,
    )
    .join("\n");

  return `// This file is generated by scripts/generate-content-index.mjs.\n// Do not edit by hand.\n\nexport const entryDetailShardLoaders = {\n${entries}\n} as const;\n\nexport type EntryDetailShardKey = keyof typeof entryDetailShardLoaders;\n`;
}

/* ---------- main ---------- */

async function buildEntryIndex() {
  const files = (await fs.readdir(entriesDirectory))
    .filter((file) => file.endsWith(".mdx"))
    .sort();

  const rawEntriesWithFiles = await Promise.all(
    files.map(async (filename) => {
      const source = await fs.readFile(path.join(entriesDirectory, filename), "utf8");
      const { data, content } = matter(source);

      // Apply defaults
      const entry = {
        ...data,
        aliases: data.aliases ?? [],
        related: data.related ?? [],
        seeAlso: data.seeAlso ?? [],
        vendorReferences: data.vendorReferences ?? [],
        tags: data.tags ?? [],
        isVendorTerm: data.isVendorTerm ?? false,
        misunderstoodScore: data.misunderstoodScore ?? 3,
        translations: data.translations ?? [],
        body: content.trim(),
      };

      assertValidEntry(entry, { filename });
      return { entry, filename };
    }),
  );
  const rawEntries = rawEntriesWithFiles.map(({ entry }) => entry);
  const knownSlugs = new Set(rawEntries.map((entry) => entry.slug));

  if (knownSlugs.size !== rawEntries.length) {
    throw new Error("Duplicate entry slugs were found in content/entries.");
  }

  for (const { entry, filename } of rawEntriesWithFiles) {
    assertValidEntry(entry, { filename, knownSlugs });
  }

  const resolveEntryReference = createEntryReferenceResolver(rawEntries);

  // Pre-compute related entries (needs all entries present)
  scoreRelatedEntries(rawEntries);

  // Enrich entries with pre-computed fields
  const entries = rawEntries.map((entry) => ({
    ...entry,
    categorySlugs: entry.categories.map((cat) => slugify(cat)),
    url: `/dictionary/${entry.slug}`,
    searchText: buildSearchText(entry),
    relatedSlugs: entry._relatedSlugs,
    resolvedSeeAlso: buildResolvedEntryReferences(entry, "seeAlso", resolveEntryReference),
    resolvedVendorReferences: buildResolvedEntryReferences(
      entry,
      "vendorReferences",
      resolveEntryReference,
    ),
  }));

  // Remove temporary field
  for (const entry of entries) {
    delete entry._relatedSlugs;
  }

  // Sort alphabetically (the default order for getAllEntries)
  entries.sort((a, b) => a.title.localeCompare(b.title));

  // Pre-compute derived collections
  const recentEntries = [...entries]
    .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
    .slice(0, 4)
    .map((e) => e.slug);
  const latestPublishedAt =
    entries.reduce((latest, entry) => {
      if (!latest) {
        return entry.publishedAt;
      }

      return new Date(entry.publishedAt).getTime() > new Date(latest).getTime()
        ? entry.publishedAt
        : latest;
    }, null) ?? "";

  const misunderstoodEntries = [...entries]
    .sort(compareMisunderstoodEntries)
    .slice(0, 4)
    .map((e) => e.slug);

  // Letter stats
  const letterCounts = {};
  for (const entry of entries) {
    letterCounts[entry.letter] = (letterCounts[entry.letter] ?? 0) + 1;
  }
  const letterStats = "ABCDEFGHIJKLMNOPQRSTUVWXYZ".split("").map((letter) => ({
    letter,
    count: letterCounts[letter] ?? 0,
    href: `/dictionary?letter=${letter}`,
  }));

  // Category stats
  const categoryStats = categoryDefinitions.map((category) => {
    const catSlug = slugify(category.title);
    const matching = entries.filter((e) => e.categorySlugs.includes(catSlug));
    return {
      ...category,
      slug: catSlug,
      count: matching.length,
      sampleTerms: matching.slice(0, 3).map((e) => e.title),
    };
  });
  const dailyWordEntries = [...entries].sort((left, right) => {
    if (left.publishedAt !== right.publishedAt) {
      return left.publishedAt.localeCompare(right.publishedAt);
    }

    return left.slug.localeCompare(right.slug);
  });
  const dailyWordStartDate = dailyWordEntries[0]?.publishedAt ?? "";

  const featuredSlug = recentEntries[0] ?? dailyWordEntries.at(-1)?.slug ?? entries[0]?.slug;
  if (!featuredSlug) {
    throw new Error("Could not derive a featured entry from the catalog.");
  }

  const catalog = {
    entries,
    recentSlugs: recentEntries,
    misunderstoodSlugs: misunderstoodEntries,
    letterStats,
    categoryStats,
    editorialTimeZone,
    dailyWordStartDate,
    dailyWordSlugs: dailyWordEntries.map((entry) => entry.slug),
    featuredSlug,
    latestPublishedAt,
  };
  const catalogVersion = createCatalogVersion(catalog);
  let generatedAt = new Date().toISOString();

  try {
    const existingSnapshot = JSON.parse(await fs.readFile(outputFile, "utf8"));

    if (existingSnapshot.catalogVersion === catalogVersion) {
      generatedAt = existingSnapshot.generatedAt;
    }
  } catch {
    // No previous generated snapshot to reconcile against.
  }

  const snapshot = createCatalogSnapshot({
    schemaVersion,
    entryCount: entries.length,
    generatedAt,
    ...catalog,
  });
  const snapshotText = serializeCatalogSnapshot(snapshot);
  const versionedCatalogFilename = `catalog.${snapshot.catalogVersion}.json`;
  const versionManifest = {
    version: snapshot.catalogVersion,
    generatedAt: snapshot.generatedAt,
    path: `/catalog/${versionedCatalogFilename}`,
  };
  const searchIndexFilename = `search-index.${snapshot.catalogVersion}.json`;
  const searchIndexPath = `/catalog/${searchIndexFilename}`;
  const entryDetailsByShard = new Map();
  const searchIndex = {
    catalogVersion: snapshot.catalogVersion,
    generatedAt: snapshot.generatedAt,
    entries: snapshot.entries.map((entry) => ({
      slug: entry.slug,
      searchText: entry.searchText,
    })),
  };

  for (const entry of snapshot.entries) {
    const shardKey = entryDetailShardKey(entry);
    const shard = entryDetailsByShard.get(shardKey) ?? {};

    shard[entry.slug] = {
      askNext: entry.askNext,
      body: entry.body,
      example: entry.example,
      misuse: entry.misuse,
      note: entry.note,
      practicalMeaning: entry.practicalMeaning,
      relatedSlugs: entry.relatedSlugs,
      seeAlso: entry.seeAlso,
      resolvedSeeAlso: entry.resolvedSeeAlso,
      translations: entry.translations,
      vendorReferences: entry.vendorReferences,
      resolvedVendorReferences: entry.resolvedVendorReferences,
      warningLabel: entry.warningLabel,
      whyExists: entry.whyExists,
    };
    entryDetailsByShard.set(shardKey, shard);
  }

  // The web worker imports the lightweight web snapshot at cold start. Strip
  // fields that can be reconstructed or lazily hydrated so the worker stays
  // below the Cloudflare size budget, while keeping the full native snapshot
  // intact for mobile clients and OTA publishing.
  const webSnapshot = {
    ...snapshot,
    searchIndexPath,
    entries: snapshot.entries.map((entry) => {
      const {
        body,
        askNext,
        example,
        misuse,
        note,
        practicalMeaning,
        searchText,
        categorySlugs,
        related,
        relatedSlugs,
        seeAlso,
        resolvedSeeAlso,
        translations,
        url,
        vendorReferences,
        resolvedVendorReferences,
        warningLabel,
        whyExists,
        tags,
        misunderstoodScore,
        ...rest
      } = entry;
      void body;
      void askNext;
      void example;
      void misuse;
      void note;
      void practicalMeaning;
      void categorySlugs;
      void related;
      void relatedSlugs;
      void seeAlso;
      void resolvedSeeAlso;
      void translations;
      void url;
      void vendorReferences;
      void resolvedVendorReferences;
      void warningLabel;
      void whyExists;
      void searchText;
      void tags;
      void misunderstoodScore;
      return rest;
    }),
  };
  const webSnapshotText = serializeCatalogSnapshot(webSnapshot);
  const searchIndexText = `${JSON.stringify(searchIndex)}\n`;
  const detailShardKeys = [...entryDetailsByShard.keys()].sort();

  await fs.mkdir(outputDirectory, { recursive: true });
  await fs.writeFile(outputFile, snapshotText, "utf8");
  await fs.writeFile(webOutputFile, webSnapshotText, "utf8");
  await fs.rm(detailsShardDirectory, { recursive: true, force: true });
  await fs.mkdir(detailsShardDirectory, { recursive: true });
  await Promise.all(
    detailShardKeys.map((shardKey) =>
      fs.writeFile(
        path.join(detailsShardDirectory, `${shardKey}.json`),
        `${JSON.stringify(entryDetailsByShard.get(shardKey))}\n`,
        "utf8",
      ),
    ),
  );
  await fs.writeFile(detailsShardLoaderFile, buildEntryDetailShardLoaderSource(detailShardKeys), "utf8");
  await fs.mkdir(publicCatalogDirectory, { recursive: true });

  const publishedCatalogFiles = await fs.readdir(publicCatalogDirectory);
  await Promise.all(
    publishedCatalogFiles
      .filter((filename) => /^catalog\.[a-f0-9]{64}\.json$/.test(filename))
      .filter((filename) => filename !== versionedCatalogFilename)
      .map((filename) => fs.rm(path.join(publicCatalogDirectory, filename), { force: true })),
  );
  await Promise.all(
    publishedCatalogFiles
      .filter((filename) => /^search-index\.[a-f0-9]{64}\.json$/.test(filename))
      .filter((filename) => filename !== searchIndexFilename)
      .map((filename) => fs.rm(path.join(publicCatalogDirectory, filename), { force: true })),
  );

  await fs.writeFile(
    path.join(publicCatalogDirectory, versionedCatalogFilename),
    snapshotText,
    "utf8",
  );
  await fs.writeFile(
    path.join(publicCatalogDirectory, searchIndexFilename),
    searchIndexText,
    "utf8",
  );
  await fs.writeFile(
    path.join(publicCatalogDirectory, "version.json"),
    `${JSON.stringify(versionManifest, null, 2)}\n`,
    "utf8",
  );
  const { manifest: mobileCatalogManifest } = await publishMobileCatalogArtifacts({
    snapshotSourceFile: outputFile,
    outputDirectory: publicMobileCatalogDirectory,
  });

  console.log(
    `Generated ${entries.length} dictionary entries into ${path.relative(root, outputFile)}`,
  );
  console.log(
    `Published catalog manifest public/catalog/version.json -> ${versionManifest.path}`,
  );
  console.log(
    `Published mobile catalog manifest public/mobile-catalog/manifest.json -> ${mobileCatalogManifest.snapshotPath}`,
  );
}

buildEntryIndex().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
